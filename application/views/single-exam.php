            <h2 id="JEE Advanced Eligibility">Eligibility for JEE Advanced 2020</h2>
            <p>Both Indian and foreign nationals (including OCI/PIO) are eligible to apply for JEE Advanced 2020. However, foreign candidates are&nbsp;not required to take&nbsp;JEE Main and may register for JEE Advanced directly.</p>
            <ol>
            <li><strong>Performance in JEE Main 2020:</strong>&nbsp;Top 2, 50,000 All India Rank holders in JEE Main 2020.</li>
            <li><strong>Age:&nbsp;</strong>Candidates should have been born on or after October 1, 1995. Five years relaxation is given to SC, ST and PwD candidates, i.e. these candidates should have been born on or after October 1, 1990.</li>
            <li><strong>Number of Attempts:</strong>&nbsp;Only 2 times in 2 consecutive years</li>
            <li><strong>Appearance in Class 12 (or equivalent):&nbsp;</strong>Candidates should have appeared for the Class XII (or equivalent) examination for the first time in either 2019 or 2020 with Physics, Chemistry, and Mathematics as compulsory subjects.</li>
            <li><strong>Earlier Admission at IITs:</strong></li>
            </ol>
            <ul>
            <li>Students who rejected the seat of IIT earlier are not eligible to apply.</li>
            <li>Those who joined preparatory courses in any of the IITs in the previous academic sessions can also apply.</li>
            <li>And those who have paid the seat acceptance fee but still not accepted the seat are also eligible.</li>
            </ul>
            <ol>
            <li value="7"><strong>Performance in Class 12 (or equivalent):</strong></li>
            </ol>
            <ul>
            <li>This criteria has been dropped for this year.</li>
            <li>As per the criteria, minimum 75% marks in 10+2 or equivalent or they must be within the category-wise top 20 percentile of qualified candidates in their particular 10+2 or equivalent examination.</li>
            <li>However, this year, due to cancellation of several class 12<sup>th</sup>&nbsp;board papers, admissions will be granted to those who qualify JEE Advanced 2020 irrespective of their marks in class 12<sup>th</sup>.&nbsp;</li>
            </ul>
