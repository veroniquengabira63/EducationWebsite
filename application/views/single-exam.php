            <h2 id="2">Difference b/w JEE Main and JEE Advanced</h2>
            <p>JEE Main is the eligibility test for appearing in JEE Advanced examination. Check the following differences between the two:</p>
            <ul>
            <li><strong>Conducting Body:&nbsp;</strong>From 2020 onwards, JEE Main is conducted by NTA. But JEE Advanced is held by one of the seven Zonal Coordinating (ZC) IITs on a rotational basis, under the guidance of JAB every year.&nbsp;<a href="#" target="_blank"><strong>Know More About NTA</strong></a></li>
            <li><strong>Eligibility:&nbsp;</strong>Top 2.50&nbsp;Lakh&nbsp;JEE Main qualifying candidates&nbsp;are eligible to appear for the test. NTA&nbsp;defines the JEE Main eligibility on specific criteria such as qualifying exams, passing marks, etc.</li>
            <li><strong>Number of Attempts:&nbsp;</strong>A student can appear for JEE Main for 3 consecutive years&nbsp;while for JEE Advanced, he/she can appear for only 2 consecutive times.</li>
            <li><strong>Exam Pattern:&nbsp;</strong>NTA&nbsp;defines the exam pattern for JEE Main, whereas there is no fixed pattern for JEE Advanced.&nbsp;</li>
            </ul>
