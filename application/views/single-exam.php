            <li>JEE (Advanced) is&nbsp;conducted to fill 10,988 seats in 23 IITs and IISC Bangalore.</li>
            <li>JAB formed by the participating IITs is the regulatory body of JEE Advanced.</li>
            <li>JEE Advanced&nbsp;Paper 1 will start at 9.00 A.M. and end at 12 noon (IST).</li>
            <li>Paper 2 will be conducted from 2.30 P.M. to 5.30 P.M. (IST).</li>
            <li>Architecture Aptitude Test (AAT) is conducted after the JEE Advanced test.&nbsp;<a href="#" target="_blank"><strong>Read More</strong></a></li>
